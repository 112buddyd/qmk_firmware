void write_layer(char layer[]) {
    oled_set_cursor(0,1);
    oled_write(layer, false);
}

void process_layer_state(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            write_layer("QWERTY");
            break;
        case 1:
            write_layer("COLEMAK");
            break;
        case 2:
            write_layer("CONTROL");
            break;
        case 3:
            write_layer("NUMPAD");
            break;
        default:
            render_logo();
            break;
    }
}

